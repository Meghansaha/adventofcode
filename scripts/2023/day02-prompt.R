# Advent of Code - Day 1-------------------------------------------------------

#=============================================================================#
# Project Options--------------------------------------------------------------
#=============================================================================#
day_num <- 2
year_num <- 2023

#=============================================================================#
# Library Load-in--------------------------------------------------------------
#=============================================================================#
library(tidyverse)
library(rvest)
library(glue)

#=============================================================================#
# Data Load-in-----------------------------------------------------------------
#=============================================================================#
# Prompt Web Scraping----------------------------------------------------------
## Pulling Prompt Site----
prompt_path <-
  glue("https://adventofcode.com/{year_num}/day/{day_num}") |>
  read_html()

## Pulling Prompt Title----
prompt_title <-
  prompt_path |>
  html_element("h2") |>
  html_text2()

## Pulling Prompt Text---- 
prompt_text <-
  prompt_path |>
  html_element("article") |>
  html_text2()

